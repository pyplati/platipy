    def forward(self, x):
        self.prob_unet.forward(x, None, False)
        return x

    def configure_optimizers(self):
        optimizer = torch.optim.Adam(
            self.parameters(), lr=self.hparams.learning_rate, weight_decay=0
        )

        scheduler = torch.optim.lr_scheduler.LambdaLR(
            optimizer, lr_lambda=[lambda epoch: self.hparams.lr_lambda ** (epoch)]
        )

        return [optimizer], [scheduler]


    def training_step(self, batch, _):

        x, y, m, _ = batch

        # Add background layer for one-hot encoding
        #y = torch.unsqueeze(y, dim=1)
        not_y = y.max(axis=1).values.logical_not()
        not_y = torch.unsqueeze(not_y, dim=1)
        y = torch.cat((not_y, y), dim=1).float()

        # self.prob_unet.forward(x, y, training=True)
        if self.hparams.prob_type == "prob":
            self.prob_unet.forward(x, y, training=True)
        else:
            self.prob_unet.forward(x, y)

        if self.hparams.prob_type == "prob":
            loss = self.prob_unet.loss(y, mask=m)
        else:
            loss = self.prob_unet.loss(x, y, mask=m)

        training_loss = loss["loss"]

        if self.hparams.prob_type == "prob":
            reg_loss = (
                l2_regularisation(self.prob_unet.posterior)
                + l2_regularisation(self.prob_unet.prior)
                + l2_regularisation(self.prob_unet.fcomb.layers)
            )
            training_loss = training_loss + 1e-5 * reg_loss
        self.log(
            "training_loss",
            training_loss.detach(),
            on_step=True,
            on_epoch=False,
            prog_bar=True,
            logger=True,
        )

        self.kl_div = loss["kl_div"].detach().cpu()

        for k in loss:
            if k == "loss":
                continue
            self.log(
                k,
                loss[k].detach() if isinstance(loss[k], torch.Tensor) else loss[k],
                on_step=True,
                on_epoch=False,
                prog_bar=True,
                logger=True,
            )
        return loss

    def validation_step(self, batch, _):

        n = 4
        m = 4

        with torch.set_grad_enabled(False):
            x, y, _, info = batch

            # Image will be same for all in batch
            x = x[0, :, :, :].unsqueeze(0)
            x = x.repeat(m, 1, 1, 1)
            self.prob_unet.forward(x)

            pred_y = self.prob_unet.sample(testing=True)
            pred_y = pred_y.to("cpu")
            y = y.to("cpu")

            # Intersection over Union (also known as Jaccard Index)
            jaccard = JaccardIndex(num_classes=2)
            term_1 = 0
            for i in range(n):
                for j in range(m):
                    if pred_y[i,:,:,:].sum() == 0 and y[j,:,:,:].sum() == 0: continue
                    iou = jaccard(pred_y[i,:,:,:].unsqueeze(0), y[j,:,:,:].unsqueeze(0))
                    term_1 += 1 - iou
            term_1 = term_1 * (2/(m*n))

            term_2 = 0
            for i in range(n):
                for j in range(n):
                    if pred_y[i,:,:,:].sum() == 0 and pred_y[j,:,:,:].sum() == 0: continue
                    iou = jaccard(pred_y[i,:,:,:].unsqueeze(0), pred_y[j,:,:,:].unsqueeze(0).argmax(1))
                    term_2 += 1 - iou
            term_2 = term_2 * (1/(n*n))

            term_3 = 0
            for i in range(m):
                for j in range(m):
                    if y[i,:,:,:].sum() == 0 and y[j,:,:,:].sum() == 0: continue
                    iou = jaccard(y[i,:,:,:].unsqueeze(0), y[j,:,:,:].unsqueeze(0))
                    term_3 += 1 - iou
            term_3 = term_3 * (1/(m*m))

            D_ged = term_1 - term_2 - term_3

        self.log("GED", D_ged)
        return D_ged


